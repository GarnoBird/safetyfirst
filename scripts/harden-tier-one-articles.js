function documentBullets(article) {
  return article.documents.slice(0, 6).map((document, index) => {
    const citation = article.citations[index % article.citations.length];
    const reasons = [
      "records the site-specific control expected before the task starts",
      "supports worker instruction, supervisor review, and follow-up when conditions change",
      "keeps the field method tied to the hazard, equipment, and work location",
      "helps the prime contractor and affected trades coordinate overlapping work",
      "creates a review trail if an inspection, incident, or refusal raises the issue",
      "confirms the plan, equipment, or instruction workers are expected to use",
    ];
    return `${document}: ${reasons[index % reasons.length]}. {{cite:${citation}}}`;
  });
}

function yaml(data) {
  return Object.entries(data)
    .map(([key, value]) => {
      if (Array.isArray(value)) {
        return `${key}:\n${value.map((item) => `  - "${escapeYaml(item)}"`).join("\n")}\n`;
      }
      return `${key}: "${escapeYaml(value)}"\n`;
    })
    .join("");
}

function bullets(items) {
  return items.map((item) => `- ${item}`).join("\n");
}

function numbered(items) {
  return items.map((item, index) => `${index + 1}. ${item}`).join("\n");
}

function checklist(items) {
  return items.map((item) => `- [ ] ${item}`).join("\n");
}

function escapeYaml(value) {
  return String(value).replace(/\\/g, "\\\\").replace(/"/g, '\\"');
}
